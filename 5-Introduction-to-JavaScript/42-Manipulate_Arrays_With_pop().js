/*In this lesson we learn about the .pop attribute and how it removes the last value
in an array, and assigns it to a new variable.*/
// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

