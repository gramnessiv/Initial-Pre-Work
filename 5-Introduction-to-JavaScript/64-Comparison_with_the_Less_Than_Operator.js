/*In this lesson we learn about the Less Than operator and how it also converts data
types for comparison, and how it can be used in a if statement.*/
function testLessThan(val) {
  if (val < 25) {  // Change this line
    return "Under 25";
  }

  if (val < 55) {  // Change this line
    return "Under 55";
  }

  return "55 or Over";
}

